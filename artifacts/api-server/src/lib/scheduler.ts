// One-time seed (July 2026): the Thane pre-July cleanup above also wiped
// Thane's 4 weekly "Team Leader KRA" templates, so weekly generation silently
// stopped for Thane only. Recreate them for Thane's Center Head + Team Leaders
// by copying Navi Mumbai's weekly templates. Idempotent — skips any (user,
// title) that already has a weekly task. lastRunDate is set to the PREVIOUS
// week's key so the boot generation right after this immediately creates the
// current week's occurrences. Runs in every environment (dev already seeded →
// no-op; production seeds on first boot after publish).
// Advisory-lock key so concurrent server instances (autoscale boots) cannot
// both run the seed's check-then-insert and create duplicate recurring roots
// (duplicate roots each become a chain head → weekly tasks double forever).
const THANE_WEEKLY_SEED_LOCK = 723_001;

// Users who must NEVER get auto-generated recurring tasks (weekly seed above
// AND the daily/weekly/monthly generator). This CODE baseline is a durable
// floor that survives checkpoint/rollback (DB flags proved non-durable in dev).
// User 9 (Edwin Thomas, Thane Center Head): opted out on request, July 2026.
// On top of this baseline, Boss/MIS can opt anyone out from the app via the
// per-user `autoTasksEnabled` flag (Access Control page) — see optedOutIds().
const NO_AUTO_TASKS_USER_IDS = new Set<number>([9]);

// The full opted-out set = the code baseline UNION every user whose
// `autoTasksEnabled` flag is false. Pass the already-loaded user rows to avoid
// a second query; callers that don't have them can load first.
function optedOutIds(users: { id: number; autoTasksEnabled: boolean }[]): Set<number> {
  const set = new Set<number>(NO_AUTO_TASKS_USER_IDS);
  for (const u of users) if (!u.autoTasksEnabled) set.add(u.id);
  return set;
}

async function ensureThaneWeeklyTemplates(): Promise<void> {
  await db.transaction(async (tx) => {
    // Serializes concurrent seeders; lock releases at transaction end.
    await tx.execute(sql`SELECT pg_advisory_xact_lock(${THANE_WEEKLY_SEED_LOCK})`);

    const targets = (
      await tx
        .select({ id: usersTable.id, autoTasksEnabled: usersTable.autoTasksEnabled })
        .from(usersTable)
        .where(
          and(eq(usersTable.center, "Thane Center"), inArray(usersTable.role, ["Center Head", "Team Leader"])),
        )
    ).filter((u) => !NO_AUTO_TASKS_USER_IDS.has(u.id) && u.autoTasksEnabled);
    if (!targets.length) return;

    // Source: Navi Mumbai's weekly root templates, one row per distinct title.
    const sourceRows = await tx
      .select({
        title: tasksTable.title,
        description: tasksTable.description,
        priority: tasksTable.priority,
        category: tasksTable.category,
        department: tasksTable.department,
      })
      .from(tasksTable)
      .innerJoin(usersTable, eq(usersTable.id, tasksTable.assignedTo))
      .where(
        and(
          eq(usersTable.center, "Navi Mumbai Center"),
          eq(tasksTable.type, "weekly"),
          isNull(tasksTable.parentTaskId),
          isNull(tasksTable.dueDate),
        ),
      );
    const templates = new Map<string, (typeof sourceRows)[number]>();
    for (const r of sourceRows) if (!templates.has(r.title)) templates.set(r.title, r);
    if (!templates.size) return;

    // Existence check is deliberately BROAD (any weekly task for that user +
    // title, template or occurrence): if occurrences exist the chain is alive
    // (generation clones whatever head has no child, even with a dangling
    // parentTaskId), so adding a new root would create a SECOND chain and
    // double-generate. Only seed when the user has nothing for that title.
    const targetIds = targets.map((u) => u.id);
    const existing = await tx
      .select({ assignedTo: tasksTable.assignedTo, title: tasksTable.title })
      .from(tasksTable)
      .where(and(inArray(tasksTable.assignedTo, targetIds), eq(tasksTable.type, "weekly")));
    const have = new Set(existing.map((t) => `${t.assignedTo}|${t.title}`));

    // Previous week's period key (Monday of last week) so generation fires now.
    const prevWeek = periodKey("weekly", new Date(Date.now() - 7 * 24 * 60 * 60 * 1000));

    const rows: (typeof tasksTable.$inferInsert)[] = [];
    for (const u of targets) {
      for (const tp of templates.values()) {
        if (have.has(`${u.id}|${tp.title}`)) continue;
        rows.push({
          title: tp.title,
          description: tp.description,
          assignedTo: u.id,
          assignedBy: null,
          priority: tp.priority,
          status: "pending",
          type: "weekly",
          category: tp.category,
          department: tp.department,
          autoGenerated: false,
          lastRunDate: prevWeek,
        });
      }
    }
    if (!rows.length) return;
    await tx.insert(tasksTable).values(rows);
    logger.info({ seeded: rows.length }, "Seeded missing Thane weekly templates");
  });
}

// ── Sonali (Accounts) JD task seed (July 2026) ─────────────────
// The user provided Sonali Pawar's full accounts JD sheet (Monthly fixed /
// Daily / month-end Audit Reports per company). Prod DB is read-only to the
// agent, so — like the Thane weekly seed — this ships as an idempotent boot
// seed: it inserts each missing head task (matched by exact title) for the
// user looked up by username. Each seed row is its OWN first occurrence
// (dueDate = next upcoming date from its rule, lastRunDate = that date's
// period key), so the generator clones only from the NEXT period onward.
// GSTR-1 / GSTR-3B rows from the sheet are intentionally omitted — they
