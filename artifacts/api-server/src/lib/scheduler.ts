// Next upcoming due date for a seed item (never in the past, so the seeded
// first occurrence isn't born overdue) + the matching period key. Daily seeds
// advance to the user's next WORKING day so a Sunday/off-Saturday boot never
// plants an off-day daily task (seed rows are autoGenerated=false, so the
// weekend boot-cleanup would not remove them).
function jdSeedDue(
  item: JdSeedItem,
  user: { center: string | null; department: string | null },
): { due: string; period: string } {
  const today = istToday();
  const freq = TYPE_TO_FREQ[item.type] ?? "Monthly";
  let due: Date | null = null;
  if (item.type === "daily") {
    const d = new Date(today);
    while (isNonWorkingDayFor(user, ymd(d))) d.setDate(d.getDate() + 1);
    due = d;
  } else if (item.dueRule) {
    due = computeDueDate({ dueDateText: item.dueRule, frequency: freq }, today);
  }
  if (!due || ymd(due) < ymd(today)) {
    if (item.type === "monthly" && item.dueRule) {
      const firstOfNext = new Date(today.getFullYear(), today.getMonth() + 1, 1);
      due = computeDueDate({ dueDateText: item.dueRule, frequency: freq }, firstOfNext) ?? firstOfNext;
    } else {
      due = today;
    }
  }
  return { due: ymd(due), period: periodKey(item.type, due) };
}

async function ensureSonaliJdTasks(): Promise<void> {
  await db.transaction(async (tx) => {
    await tx.execute(sql`SELECT pg_advisory_xact_lock(${SONALI_JD_SEED_LOCK})`);

    const [user] = await tx
      .select({
        id: usersTable.id,
        autoTasksEnabled: usersTable.autoTasksEnabled,
        center: usersTable.center,
        department: usersTable.department,
      })
      .from(usersTable)
      .where(eq(usersTable.username, SONALI_USERNAME));
    if (!user || NO_AUTO_TASKS_USER_IDS.has(user.id) || !user.autoTasksEnabled) return;

    // Broad existence check (any task with that exact title, template or
    // occurrence) — same reasoning as the Thane seed: a live chain must never
    // get a second root or it double-generates.
    const existing = await tx
      .select({ title: tasksTable.title })
      .from(tasksTable)
      .where(eq(tasksTable.assignedTo, user.id));
    const have = new Set(existing.map((t) => t.title));

    const rows: (typeof tasksTable.$inferInsert)[] = [];
    for (const item of SONALI_JD_TASKS) {
      if (have.has(item.title)) continue;
      const { due, period } = jdSeedDue(item, user);
      rows.push({
        title: item.title,
        assignedTo: user.id,
        assignedBy: null,
        priority: "medium",
        status: "pending",
        type: item.type,
        category: "Compliance",
        department: "Accounts",
        dueDate: due,
        dueRule: item.dueRule,
        autoGenerated: false,
        lastRunDate: period,
      });
    }
    if (!rows.length) return;
    await tx.insert(tasksTable).values(rows);
    logger.info({ seeded: rows.length }, "Seeded missing Sonali JD tasks");
  });
}

// Boot cleanup (July 2026 weekend policy): remove auto-generated DAILY
// occurrences that were created on a non-working day for their assignee
// (any Sunday, any 1st Saturday, Head Office non-MIS Saturdays) — these
// existed before the weekend skip was added and clutter the list as fake
// overdue work. Only pending, auto-generated, non-compliance occurrences are
// touched: anything marked done stays (the person actually worked that day),
// and self-serve occurrences (autoGenerated=false via "Generate Today's
// Tasks") always stay. Children are re-parented to the doomed row's own
// parent so recurrence chains stay intact; processed newest-first so a
// doomed child is re-linked before its (also doomed) parent goes away.
// Idempotent + generic — safe to run on every boot.
async function cleanupNonWorkingDayDailyTasks(): Promise<void> {
  const users = await db.select().from(usersTable);
  const userById = new Map(users.map((u) => [u.id, u]));

  const rows = await db
    .select()
    .from(tasksTable)
    .where(
      and(
        eq(tasksTable.type, "daily"),
        eq(tasksTable.autoGenerated, true),
        eq(tasksTable.status, "pending"),
        isNull(tasksTable.complianceItemId),
        isNotNull(tasksTable.parentTaskId),
        isNotNull(tasksTable.lastRunDate),
      ),
    );

  const doomed = rows
    .filter(
      (t) =>
        /^\d{4}-\d{2}-\d{2}$/.test(t.lastRunDate!) &&
        isNonWorkingDayFor(t.assignedTo != null ? userById.get(t.assignedTo) : undefined, t.lastRunDate!),
    )
    .sort((a, b) => (a.lastRunDate! < b.lastRunDate! ? 1 : -1)); // newest first

  if (!doomed.length) return;

  for (const t of doomed) {
    // Keep the chain linked: children point to this row's own parent instead.
    await db
      .update(tasksTable)
      .set({ parentTaskId: t.parentTaskId })
      .where(eq(tasksTable.parentTaskId, t.id));
    await db.delete(notificationsTable).where(eq(notificationsTable.taskId, t.id));
    await db.delete(taskTransfersTable).where(eq(taskTransfersTable.taskId, t.id));
    await db.delete(tasksTable).where(eq(tasksTable.id, t.id));
  }
  logger.info({ removed: doomed.length }, "Cleaned up daily tasks generated on non-working days");
}

// ── Daily digest ───────────────────────────────────────────────
// One combined email per user listing the tasks relevant for today:
// everything still open that is due today or overdue.
export async function sendDailyDigest(): Promise<{ sent: number; eligible: number; skippedNoEmail: number; failedSend: number }> {
  logger.info("Running daily digest job");
  const today = istDateStr();
  const users = await db.select().from(usersTable);
  let sent = 0;
  let eligible = 0;
  let skippedNoEmail = 0;
  let failedSend = 0;

  for (const user of users) {
    const tasks = await db
      .select()
      .from(tasksTable)
      .where(and(eq(tasksTable.assignedTo, user.id), ne(tasksTable.status, "done")));

    const relevant = tasks.filter((t) => t.dueDate && t.dueDate <= today);
    if (!relevant.length) continue;

    // This user has tasks worth a reminder, but can only receive one if they
    // have an email address on file.
    eligible++;
    if (!user.email) { skippedNoEmail++; continue; }

    const ok = await sendDailyDigestEmail({
      toEmail: user.email,
      toName: user.name,
      dateStr: today,
      tasks: relevant
        .map((t) => ({
          title: t.title,
          status: t.status,
          priority: t.priority,
          type: t.type,
          dueDate: t.dueDate,
          overdue: !!t.dueDate && t.dueDate < today,
        }))
        .sort((a, b) => Number(b.overdue) - Number(a.overdue)),
    });
    if (ok) sent++;
    else failedSend++;

    // Pace sends to stay under Resend's 2 requests/second limit.
    await new Promise((r) => setTimeout(r, 600));
  }

  logger.info({ sent, eligible, skippedNoEmail, failedSend }, "Daily digest complete");
  return { sent, eligible, skippedNoEmail, failedSend };
}

// ── Daily dashboard summary ────────────────────────────────────
// A morning snapshot of the live Dashboard (KPI cards + by-category /
// by-team-member breakdowns) emailed to a fixed set of people. Each recipient's
// numbers are scoped to exactly what they're entitled to see on the Dashboard
// page (Boss/MIS get everything; others get their own team), via scopeTasks.
// Keyed by stable user id (not name) so duplicate names can never misroute a
// summary to the wrong person/email.
const DASHBOARD_RECIPIENT_IDS = [
  6, // Vikas Gupta (MIS)
  9, // Edwin Thomas (Center Head, Thane)
  13, // Prakash Thakur (Team Leader, Thane)
  14, // Riyaz Khan (Team Leader, Thane)
  15, // Swapnil Reddy (Team Leader, Thane)
  16, // Sujit Yadav (Team Leader, Thane)
];

export async function sendDashboardDigest(): Promise<{ sent: number; eligible: number; skippedNoEmail: number; failedSend: number }> {
  logger.info("Running daily dashboard summary job");
  const today = istDateStr();
  const users = await db.select().from(usersTable);
  const allTasks = await db.select().from(tasksTable);
  let sent = 0;
  let eligible = 0;
  let skippedNoEmail = 0;
  let failedSend = 0;

  for (const id of DASHBOARD_RECIPIENT_IDS) {
    const recipient = users.find((u) => u.id === id);
    if (!recipient) {
      logger.warn({ id }, "Dashboard recipient not found");
      continue;
    }
    eligible++;
    if (!recipient.email) { skippedNoEmail++; continue; }

    const scoped = scopeTasks(recipient, users, allTasks);
    const total = scoped.length;
    const pending = scoped.filter((t) => t.status === "pending").length;
    const inProgress = scoped.filter((t) => t.status === "inProgress").length;
    const done = scoped.filter((t) => t.status === "done").length;
    const completionRate = total > 0 ? Math.round((done / total) * 100) : 0;

    const catMap = new Map<string, { count: number; done: number }>();
    for (const t of scoped) {
      const cat = t.category ?? "Uncategorized";
      if (!catMap.has(cat)) catMap.set(cat, { count: 0, done: 0 });
      const e = catMap.get(cat)!;
      e.count++;
      if (t.status === "done") e.done++;
    }

    const userMap = new Map<number, { name: string; department: string; total: number; done: number; pending: number }>();
    for (const u of users) userMap.set(u.id, { name: u.name, department: u.department, total: 0, done: 0, pending: 0 });
    for (const t of scoped) {
      if (!t.assignedTo) continue;
      const e = userMap.get(t.assignedTo);
      if (!e) continue;
      e.total++;
      if (t.status === "done") e.done++;
      else e.pending++;
    }

    const ok = await sendDashboardEmail({
      toEmail: recipient.email,
      toName: recipient.name,
      dateStr: today,
      total,
      pending,
      inProgress,
      done,
      completionRate,
      byCategory: Array.from(catMap.entries()).map(([category, v]) => ({ category, ...v })),
      byUser: Array.from(userMap.values()).filter((v) => v.total > 0),
    });
    if (ok) sent++;
    else failedSend++;

    // Pace sends to stay under Resend's 2 requests/second limit.
    await new Promise((r) => setTimeout(r, 600));
  }

  logger.info({ sent, eligible, skippedNoEmail, failedSend }, "Daily dashboard summary complete");
  return { sent, eligible, skippedNoEmail, failedSend };
}

// Calendar arithmetic on a "YYYY-MM-DD" string, timezone-safe (pure UTC math).
function addDaysStr(dateStr: string, delta: number): string {
  const [y, m, d] = dateStr.split("-").map(Number);
  const dt = new Date(Date.UTC(y, m - 1, d));
  dt.setUTCDate(dt.getUTCDate() + delta);
  return dt.toISOString().slice(0, 10);
}

// The day a due-date reminder should fire: two working (non-holiday) days
// before the due date, landing on a non-holiday day. A holiday inside the lead
// window moves the reminder earlier so it always arrives BEFORE the holiday —
// important for Accounts (payment / report) tasks whose due date can't slip.
function reminderDateFor(dueDate: string, holidaySet: Set<string>): string {
  let d = dueDate;
  let lead = 0;
  while (lead < 2) {
    d = addDaysStr(d, -1);
    if (!holidaySet.has(d)) lead++;
  }
  while (holidaySet.has(d)) d = addDaysStr(d, -1);
  return d;
}

// ── Due-date reminders (holiday-aware, ~two working days out) ──────────────
// Twice a day, find every open task whose holiday-aware reminder day is today
// and remind the people responsible: each assignee gets their own list (in-app
// notification + email) and the Boss gets a single consolidated list of all
// such tasks across the team. `period` is a human label ("Morning"/"Afternoon").
export async function sendDueDateReminders(
  period: string,
): Promise<{ tasks: number; notified: number; emailed: number }> {
  logger.info({ period }, "Running due-date reminder job");
  const todayStr = ymd(istToday());
  const holidayRows = await db.select().from(holidaysTable);
  // Office is off on full-day and weekend-type holidays; half-days still count
  // as working days, so only those are skipped when counting the 2-day lead.
  const holidaySet = new Set(holidayRows.filter((h) => h.type !== "half").map((h) => h.date));

  const users = await db.select().from(usersTable);
  const userMap = new Map(users.map((u) => [u.id, u]));
  const openTasks = await db
    .select()
    .from(tasksTable)
    .where(and(isNotNull(tasksTable.dueDate), ne(tasksTable.status, "done")));
  // Fire today for every task whose holiday-aware reminder date is today.
  const due = openTasks.filter(
    (task) => task.dueDate != null && reminderDateFor(task.dueDate, holidaySet) === todayStr,
  );

  if (!due.length) {
    logger.info({ period, today: todayStr, tasks: 0 }, "Due-date reminder: nothing due soon");
    return { tasks: 0, notified: 0, emailed: 0 };
  }

  const byAssignee = new Map<number, typeof due>();
  for (const task of due) {
    if (!task.assignedTo) continue;
    const list = byAssignee.get(task.assignedTo);
    if (list) list.push(task);
    else byAssignee.set(task.assignedTo, [task]);
  }

  let notified = 0;
  let emailed = 0;
  const toRow = (task: (typeof due)[number]) => ({
    title: task.title,
    status: task.status,
    priority: task.priority,
    dueDate: task.dueDate,
  });

  // Per-assignee: in-app notification + email.
  for (const [uid, list] of byAssignee) {
    const u = userMap.get(uid);
    if (!u) continue;
    const plural = list.length === 1 ? "task" : "tasks";
    await createNotifications([{
      userId: uid,
      type: "due_reminder",
      message: `Reminder: ${list.length} ${plural} due soon — please complete before the due date.`,
      taskId: list[0].id,
    }]);
    notified++;
    if (u.email) {
      const ok = await sendReminderEmail({
        toEmail: u.email,
        toName: u.name,
        period: `Due Soon (${period})`,
        tasks: list.map(toRow),
      });
      if (ok) emailed++;
      await new Promise((r) => setTimeout(r, 600));
    }
  }

  // Boss / Management: one consolidated list of everything due in 2 days.
  const bosses = users.filter((u) => isBoss(u));
  for (const b of bosses) {
    const plural = due.length === 1 ? "task" : "tasks";
    await createNotifications([{
      userId: b.id,
      type: "due_reminder",
      message: `Team reminder: ${due.length} ${plural} due soon.`,
      taskId: due[0].id,
    }]);
    notified++;
    if (b.email) {
      const ok = await sendReminderEmail({
        toEmail: b.email,
        toName: b.name,
        period: `Team — Due Soon (${period})`,
        tasks: due.map(toRow),
      });
      if (ok) emailed++;
      await new Promise((r) => setTimeout(r, 600));
    }
  }

  logger.info({ period, today: todayStr, tasks: due.length, notified, emailed }, "Due-date reminder complete");
  return { tasks: due.length, notified, emailed };
}

export function startScheduler() {
  const opts = { timezone: SCHEDULER_TZ } as const;
  // Catch-up on boot: node-cron does not replay missed runs, so if the server
  // was down/restarted across a midnight boundary the day's occurrences would
  // never be generated. Running once at startup fills any missed period. The
  // job is idempotent (period-key guard + onConflictDoNothing), so this is safe.
  // Retire the old joke "lunch" tasks first, then run recurring generation so
  // they are not re-cloned. They now live as a dashboard banner + 12/1 PM bell.
  retireLunchTasks()
    .catch((err) => logger.error({ err }, "Lunch task retirement failed"))
    .then(() =>
      cleanupThanePreJulyTasks().catch((err) =>
        logger.error({ err }, "Thane pre-July task cleanup failed"),
      ),
    )
    .then(() =>
      ensureThaneWeeklyTemplates().catch((err) =>
        logger.error({ err }, "Thane weekly template seed failed"),
      ),
    )
    .then(() =>
      ensureSonaliJdTasks().catch((err) =>
        logger.error({ err }, "Sonali JD task seed failed"),
      ),
    )
    .then(() =>
      cleanupOptedOutUserTasks().catch((err) =>
        logger.error({ err }, "Opted-out user task cleanup failed"),
      ),
    )
    .then(() =>
      cleanupNonWorkingDayDailyTasks().catch((err) =>
        logger.error({ err }, "Non-working-day daily task cleanup failed"),
      ),
    )
    .finally(() => {
      generateRecurringTasks().catch((err) =>
        logger.error({ err }, "Boot catch-up recurring generation failed"),
      );
    });
  // Generate recurring occurrences just after midnight IST...
  cron.schedule(
    "5 0 * * *",
    () => {
      void generateRecurringTasks();
    },
    opts,
  );
  // ...then send everyone their combined "today's tasks" digest at 9:00 AM IST.
  // Guarded by a once-per-day claim so it never double-sends alongside the
  // Scheduled Deployment job (see runDailyDigestOnce).
  cron.schedule("0 9 * * *", () => void runDailyDigestOnce(), opts);
  // ...and a daily Dashboard summary to selected people at 9:00 AM IST.
  cron.schedule("0 9 * * *", () => void runDashboardDigestOnce(), opts);
  // Due-date reminders (tasks due in exactly 2 days) twice a day: 10 AM + 3 PM IST.
  cron.schedule("0 10 * * *", () => void sendDueDateReminders("Morning"), opts);
  cron.schedule("0 15 * * *", () => void sendDueDateReminders("Afternoon"), opts);
  // Fun lunch reminders for everyone (in-app bell only): order at 12, eat at 1.
  cron.schedule("0 12 * * *", () => void runLunchReminderOnce("order"), opts);
  cron.schedule("0 13 * * *", () => void runLunchReminderOnce("eat"), opts);
  logger.info("Task scheduler started (boot catch-up + recurring generation + daily digest + due-date reminders + lunch reminders)");
}
