// ── Auto-generate recurring task occurrences ───────────────────
// Every daily/weekly/monthly task is recurring. Each chain has a single
// "head" (the latest occurrence — no other task points to it via
// parentTaskId). Once per period we clone the head into a fresh pending
// occurrence dated today, preserving history. `lastRunDate` (a period
// key) guards against generating the same period twice.
// Maps the task `type` to the capitalized frequency the due-date parser expects.
const TYPE_TO_FREQ: Record<string, string> = {
  daily: "Daily",
  weekly: "Weekly",
  monthly: "Monthly",
  quarterly: "Quarterly",
  annual: "Annual",
};

// Clone a chain head into a fresh pending occurrence for the current period.
// Returns true when a new row was actually inserted (false = the
// (parent_task_id, last_run_date) guard already had this period).
// `autoGenerated: false` marks occurrences a user explicitly requested via
// "Generate Today's Tasks" so the weekend boot-cleanup never removes them.
async function cloneRecurringHead(
  head: typeof tasksTable.$inferSelect,
  cur: string,
  opts: { autoGenerated: boolean },
): Promise<number | null> {
  // Recurring tasks carrying a due-date rule recompute the occurrence's due
  // date for the new period (e.g. "6th of next month" -> the 6th this month);
  // ad-hoc recurring tasks keep defaulting to the generation day.
  let due = istDateStr();
  if (head.dueRule) {
    const computed = computeDueDate(
      { dueDateText: head.dueRule, frequency: TYPE_TO_FREQ[head.type] ?? "Monthly" },
      istToday(),
    );
    if (computed) due = ymd(computed);
  }

  const inserted = await db
    .insert(tasksTable)
    .values({
      title: head.title,
      description: head.description,
      assignedTo: head.assignedTo,
      assignedBy: head.assignedBy,
      dueDate: due,
      dueRule: head.dueRule,
      priority: head.priority,
      status: "pending",
      type: head.type,
      category: head.category,
      department: head.department,
      // Each new occurrence starts with a FRESH remark — yesterday's remark
      // belongs to yesterday's task (user request, July 2026).
      remark: null,
      parentTaskId: head.id,
      autoGenerated: opts.autoGenerated,
      lastRunDate: cur,
    })
    .onConflictDoNothing({
      target: [tasksTable.parentTaskId, tasksTable.lastRunDate],
      where: sql`parent_task_id IS NOT NULL`,
    })
    .returning({ id: tasksTable.id });
  return inserted.length > 0 ? inserted[0].id : null;
}

export async function generateRecurringTasks(): Promise<number> {
  logger.info("Running recurring task generation job");
  const all = await db.select().from(tasksTable);
  const hasChild = new Set<number>();
  for (const t of all) if (t.parentTaskId != null) hasChild.add(t.parentTaskId);

  const users = await db.select().from(usersTable);
  const userById = new Map(users.map((u) => [u.id, u]));
  const optedOut = optedOutIds(users);

  const today = istDateStr();
  let created = 0;
  // Per-user tally of freshly generated DAILY occurrences so we can push one
  // "your daily tasks are ready" notification per person (not one per task).
  const dailyByUser = new Map<number, { count: number; taskId: number }>();

  for (const head of all) {
    if (head.complianceItemId != null) continue; // compliance tasks recur via generateComplianceTasks
    if (!isRecurringType(head.type)) continue;
    if (head.assignedTo != null && optedOut.has(head.assignedTo)) continue; // opted-out user
    if (hasChild.has(head.id)) continue; // not the latest occurrence in its chain
    const cur = periodKey(head.type);

    // Initialize an un-tracked head without generating a duplicate this period.
    if (!head.lastRunDate) {
      await db.update(tasksTable).set({ lastRunDate: cur }).where(eq(tasksTable.id, head.id));
      continue;
    }
    if (cur <= head.lastRunDate) continue; // current period already generated

    // Weekend policy: DAILY occurrences are skipped on the assignee's
    // non-working day (Sunday for everyone / 1st Saturday for everyone /
    // other Saturdays for Head Office non-MIS). lastRunDate is left
    // untouched, so the next working day generates normally. Someone who IS
    // working can self-serve via "Generate Today's Tasks" (see
    // generateMyDailyTasks). Weekly/monthly/etc. still generate — they span
    // the whole period, not a single day.
    if (head.type === "daily") {
      const assignee = head.assignedTo != null ? userById.get(head.assignedTo) : undefined;
      if (isNonWorkingDayFor(assignee, today)) continue;
    }

    const newId = await cloneRecurringHead(head, cur, { autoGenerated: true });
    if (newId != null) {
      created++;
      if (head.type === "daily" && head.assignedTo != null) {
        const prev = dailyByUser.get(head.assignedTo);
        if (prev) prev.count++;
        else dailyByUser.set(head.assignedTo, { count: 1, taskId: newId });
      }
    }
  }

  // One in-app + push notification per user whose daily tasks were generated.
  // Guarded by the same once-per-day claim lock as the digest jobs: generation
  // itself is idempotent (onConflictDoNothing), but concurrent runs (boot +
  // cron, or two instances) could each create disjoint subsets and each try to
  // notify — the claim makes the notification at-most-once per IST day.
  if (dailyByUser.size > 0 && (await claimScheduledJob("daily-task-notify", today))) {
    try {
      await createNotifications(
        [...dailyByUser.entries()].map(([userId, { count, taskId }]) => ({
          userId,
          type: "daily_tasks",
          message:
            count === 1
              ? "Your daily task for today has been generated. Please check your task list."
              : `Your ${count} daily tasks for today have been generated. Please check your task list.`,
          taskId,
        })),
      );
    } catch (err) {
      await releaseScheduledJob("daily-task-notify", today).catch(() => {});
      throw err;
    }
  }

  logger.info({ created }, "Recurring task generation complete");
  return created;
}

// Self-serve generation for someone who IS working on an off day (Sunday /
// 1st Saturday): clone TODAY's occurrence of every daily chain assigned to
// this user, ignoring the weekend skip. Idempotent — the same period-key +
// unique-index guards apply, so clicking twice creates nothing extra. Only
// the caller's own daily tasks are touched.
export async function generateMyDailyTasks(userId: number): Promise<number> {
  if (NO_AUTO_TASKS_USER_IDS.has(userId)) return 0; // opted-out (code baseline)
  const [me] = await db.select({ autoTasksEnabled: usersTable.autoTasksEnabled }).from(usersTable).where(eq(usersTable.id, userId));
  if (me && !me.autoTasksEnabled) return 0; // opted-out (admin toggle)
  const all = await db.select().from(tasksTable);
  const hasChild = new Set<number>();
  for (const t of all) if (t.parentTaskId != null) hasChild.add(t.parentTaskId);

  const cur = periodKey("daily");
  let created = 0;

  for (const head of all) {
    if (head.assignedTo !== userId) continue;
    if (head.type !== "daily") continue;
    if (head.complianceItemId != null) continue;
    if (hasChild.has(head.id)) continue;

    // Untracked head: initialize it as today's occurrence (same as the
    // scheduled job would).
    if (!head.lastRunDate) {
      await db.update(tasksTable).set({ lastRunDate: cur }).where(eq(tasksTable.id, head.id));
      continue;
    }
    if (cur <= head.lastRunDate) continue; // today's occurrence already exists

    if ((await cloneRecurringHead(head, cur, { autoGenerated: false })) != null) created++;
  }

  logger.info({ userId, created }, "Self-serve daily task generation complete");
  return created;
}

// ── Auto-generate compliance task occurrences ──────────────────
// Mirror every active Compliance Calendar activity into a real task for the
// current period so the Accounts team can see and complete it in All Tasks. One
// task per item per period (idempotent via the (compliance_item_id, last_run_date)
// unique index). The task's initial status is derived from the existing
// per-company compliance grid so the two views agree from the moment it appears.
// Compliance frequencies map onto the task `type` so the All Tasks row shows the
// real cadence (Monthly/Quarterly/Annual…) instead of "One Time". Unknown values
// fall back to one_time. The recurring generator skips compliance-linked tasks
// (see generateRecurringTasks) so these types never trigger double-generation.
const COMPLIANCE_FREQ_TO_TASK_TYPE: Record<string, string> = {
  daily: "daily",
  weekly: "weekly",
  monthly: "monthly",
  quarterly: "quarterly",
  annual: "annual",
};

export async function generateComplianceTasks(): Promise<number> {
  logger.info("Running compliance task generation job");
  const items = await db.select().from(complianceItemsTable).where(eq(complianceItemsTable.active, true));
  if (!items.length) {
    logger.info({ created: 0 }, "Compliance task generation complete");
    return 0;
  }

  // Only companies still on the active master list have a tickable cell in the
  // Compliance Calendar, so initial task completion must consider exactly those
  // (mirrors Compliance.tsx + complianceSync).
  const activeCompanyRows = await db
    .select({ name: complianceCompaniesTable.name })
    .from(complianceCompaniesTable)
    .where(eq(complianceCompaniesTable.active, true));
  const activeCompanies = new Set(activeCompanyRows.map((r) => r.name));

  // Pre-index every "done" status so initial task state is computed without an
  // extra query per item.
  const statuses = await db.select().from(complianceStatusTable);
  const doneByItemPeriod = new Map<string, Set<string>>();
  for (const s of statuses) {
    if (!s.done) continue;
    const key = `${s.itemId}|${s.periodKey}`;
    let set = doneByItemPeriod.get(key);
    if (!set) {
      set = new Set<string>();
      doneByItemPeriod.set(key, set);
    }
    set.add(s.company);
  }

  const today = istToday();
  let created = 0;

  for (const item of items) {
    const applicable = item.companies.length
      ? item.companies.filter((c) => activeCompanies.has(c))
      : ["ALL"];
    const due = computeDueDate({ dueDateText: item.dueDateText, frequency: item.frequency }, today);
    const taskType = COMPLIANCE_FREQ_TO_TASK_TYPE[(item.frequency ?? "").trim().toLowerCase()] ?? "one_time";
    const pKey = periodKeyForDate(item.frequency, due ?? today);
    const doneCompanies = doneByItemPeriod.get(`${item.id}|${pKey}`) ?? new Set<string>();
    const allDone = applicable.every((c) => doneCompanies.has(c));

    const descParts = [
      item.activity?.trim() || null,
      item.companies.length ? `Companies: ${item.companies.join(", ")}` : null,
    ].filter(Boolean);

    const inserted = await db
      .insert(tasksTable)
      .values({
        title: item.compliance,
        description: descParts.length ? descParts.join(" — ") : null,
        assignedTo: item.assignedTo,
        assignedBy: null,
        dueDate: due ? ymd(due) : null,
        priority: "medium",
        status: allDone ? "done" : "pending",
        // Reflect the compliance cadence in the task type so the row shows the real
        // frequency. Double-generation is prevented in generateRecurringTasks, which
        // skips any compliance-linked task regardless of type.
        type: taskType,
        category: "Compliance",
        department: "Accounts",
        complianceItemId: item.id,
        autoGenerated: true,
        lastRunDate: pKey,
      })
      .onConflictDoNothing({
        target: [tasksTable.complianceItemId, tasksTable.lastRunDate],
        where: sql`compliance_item_id IS NOT NULL`,
      })
      .returning({ id: tasksTable.id });
    if (inserted.length) created++;
  }

  logger.info({ created }, "Compliance task generation complete");
  return created;
}

// ── Once-per-day claim lock ────────────────────────────────────
// The 9 AM digest/dashboard jobs can be fired from TWO places: the in-app cron
// (only reliable when the Autoscale app is awake) and a separate Replit
// Scheduled Deployment (which is why they exist as a standalone entry). To make
// sure each email is sent AT MOST once per day regardless of which one fires
// first, both go through this atomic claim: an INSERT ... ON CONFLICT DO NOTHING
// on the shared app_settings table keyed by (job, IST date). Whoever inserts the
// row first "owns" the run; the loser sees 0 rows and skips. Manual admin
// triggers (POST /email/send-*-now) bypass this on purpose so a Boss/MIS can
// always force a resend.
function jobLockKey(jobKey: string, dateStr: string): string {
  return `emailrun:${jobKey}:${dateStr}`;
}

async function claimScheduledJob(jobKey: string, dateStr: string): Promise<boolean> {
  const rows = await db
    .insert(appSettingsTable)
    .values({ key: jobLockKey(jobKey, dateStr), value: new Date().toISOString() })
    .onConflictDoNothing({ target: appSettingsTable.key })
    .returning({ key: appSettingsTable.key });
  return rows.length === 1;
}

// Release a claim so the day can be retried. Called when the send THROWS, so a
// crash/DB error after claiming but before delivering does not permanently
// suppress that day's emails (a later trigger — in-app cron, scheduled job, or
// the /api/cron endpoint — can retry). A hard process kill mid-send is the one
// residual edge; the Boss/MIS manual "send now" buttons bypass the lock entirely
// and cover that case.
async function releaseScheduledJob(jobKey: string, dateStr: string): Promise<void> {
  await db.delete(appSettingsTable).where(eq(appSettingsTable.key, jobLockKey(jobKey, dateStr)));
}

// Run a claimed job, releasing the claim if it throws so today can be retried.
async function runClaimedJob<T>(jobKey: string, run: () => Promise<T>): Promise<T | null> {
  const dateStr = istDateStr();
  if (!(await claimScheduledJob(jobKey, dateStr))) {
    logger.info({ jobKey }, "Job already claimed for today — skipping to avoid a duplicate send");
    return null;
  }
  try {
    return await run();
  } catch (err) {
    await releaseScheduledJob(jobKey, dateStr).catch(() => {});
    throw err;
  }
}

export async function runDailyDigestOnce(): Promise<Awaited<ReturnType<typeof sendDailyDigest>> | null> {
  return runClaimedJob("daily-digest", sendDailyDigest);
}

export async function runDashboardDigestOnce(): Promise<Awaited<ReturnType<typeof sendDashboardDigest>> | null> {
  return runClaimedJob("dashboard-digest", sendDashboardDigest);
}

// ── Fun lunch reminders (in-app only — NOT tasks, NO email) ────
// Two light-hearted daily nudges for EVERYONE via the in-app bell. These
// replace the old "Order your lunch"/"Go for lunch" entries that used to
// clutter the task list. Fired by the in-app cron when the Autoscale app is
// awake and by GET/POST /api/cron/lunch-reminder (external scheduler) otherwise;
// the once-per-day claim lock makes each at-most-once per day no matter which
// trigger fires first.
const LUNCH_REMINDERS = {
  order: { job: "lunch-order", message: "🍱 Time to order your lunch — please place your order by 12:00 PM!" },
  eat: { job: "lunch-eat", message: "🥗 It's 1:00 PM — lunch break time. Step away and enjoy your meal!" },
} as const;
export type LunchReminderKind = keyof typeof LUNCH_REMINDERS;

export function isLunchReminderKind(v: unknown): v is LunchReminderKind {
  return v === "order" || v === "eat";
}

// Titles of the retired joke "lunch" tasks — deleted on boot (see startScheduler)
// so they stop showing in the task list / digest and stop recurring. They now
// live as this in-app reminder + a dashboard banner instead.
const RETIRED_LUNCH_TITLES = ["Order your lunch at 12:00 PM", "Go for lunch at 1:00 PM"];

export async function sendLunchReminder(kind: LunchReminderKind): Promise<{ notified: number }> {
  const { message } = LUNCH_REMINDERS[kind];
  const recipients = await db
    .select({ id: usersTable.id })
    .from(usersTable)
    .where(and(eq(usersTable.status, "Active"), isNotNull(usersTable.username)));
  if (!recipients.length) return { notified: 0 };
  await createNotifications(
    recipients.map((u) => ({ userId: u.id, type: "lunch_reminder", message }))
  );
  logger.info({ kind, notified: recipients.length }, "Lunch reminder sent");
  return { notified: recipients.length };
}

export async function runLunchReminderOnce(
  kind: LunchReminderKind,
): Promise<{ notified: number } | null> {
  return runClaimedJob(LUNCH_REMINDERS[kind].job, () => sendLunchReminder(kind));
}

